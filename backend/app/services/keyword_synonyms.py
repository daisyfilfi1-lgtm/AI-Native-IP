def expand_keywords(keywords: List[str]) -> Set[str]:
    """
    扩展关键词列表，包含同义词
    
    Args:
        keywords: 原始关键词列表
        
    Returns:
        扩展后的关键词集合（包含原始词和同义词）
    """
    expanded = set()
    
    for kw in keywords:
        kw = kw.strip()
        if not kw:
            continue
            
        expanded.add(kw)
        
        # 查找同义词
        if kw in KEYWORD_SYNONYMS:
            expanded.update(KEYWORD_SYNONYMS[kw])
        
        # 反向查找（如果关键词是同义词，找到原始词并添加其他同义词）
        for original, synonyms in KEYWORD_SYNONYMS.items():
            if kw in synonyms:
                expanded.add(original)
                expanded.update(synonyms)
    
    return expanded


def calculate_keyword_match_score(text: str, keywords: List[str]) -> float:
    """
    计算文本与关键词的匹配分数（基于同义词扩展）
    
    Args:
        text: 待匹配的文本
        keywords: 关键词列表
        
    Returns:
        匹配分数 (0.0 - 1.0)
    """
    if not text or not keywords:
        return 0.0
    
    text_lower = text.lower()
    expanded_keywords = expand_keywords(keywords)
    
    if not expanded_keywords:
        return 0.0
    
    # 计算匹配的关键词数量
    matched = sum(1 for kw in expanded_keywords if kw.lower() in text_lower)
    
    # 归一化到 0-1
    return min(1.0, matched / min(len(keywords) * 1.5, len(expanded_keywords)))


def classify_content_type(title: str) -> str:
    """
    根据标题分类内容类型
    
    Args:
        title: 内容标题
        
    Returns:
        内容类型: money/emotion/skill/life/other
    """
    if not title:
        return "other"
    
    title_lower = title.lower()
    
    # 计算每种类型的匹配分数
    scores = {}
    for content_type, keywords in CONTENT_TYPE_KEYWORDS.items():
        expanded = expand_keywords(keywords)
        matched = sum(1 for kw in expanded if kw.lower() in title_lower)
        scores[content_type] = matched
    
    # 返回分数最高的类型
    if not scores:
        return "other"
    
    best_type = max(scores.items(), key=lambda x: x[1])
    
    # 如果没有匹配，返回other
    if best_type[1] == 0:
        return "other"
    
    return best_type[0]


